    public void agregarProducto(JLabel label){
        int valor;
        if(label.getText().equals("Agotado")){
            valor=0;
        }else{
            valor = Integer.parseInt(label.getText());
        }
        if(valor>0){
            valor--;
            if(valor!=0){
                label.setText(String.valueOf(valor));
            }else{
                label.setText("Agotado");
            }
        }else{
            JOptionPane.showMessageDialog(null, "El producto esta agotado");
        }
    }
    
