    private void jButtonComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonComprarActionPerformed
        int[] can = new int[10];
        can[0]=Integer.parseInt(jLabelCantidad.getText());
        can[1]=Integer.parseInt(jLabelCantidad1.getText());
        can[2]=Integer.parseInt(jLabelCantidad2.getText());
        can[3]=Integer.parseInt(jLabelCantidad3.getText());
        can[4]=Integer.parseInt(jLabelCantidad4.getText());
        can[5]=Integer.parseInt(jLabelCantidad5.getText());
        can[6]=Integer.parseInt(jLabelCantidad6.getText());
        can[7]=Integer.parseInt(jLabelCantidad7.getText());
        can[8]=Integer.parseInt(jLabelCantidad8.getText());
        can[9]=Integer.parseInt(jLabelCantidad9.getText());
        int dialog = JOptionPane.YES_NO_OPTION;
        int result = JOptionPane.showConfirmDialog(null, "Confirmar compra!", "EXIT", dialog);
        if(result == 0){
            metodo.compraProducto(can, nombre);
            JOptionPane.showMessageDialog(null, "Compra realizada con exito!");
            for(int i=0;i<can.length;i++){
                cancelar[i]=String.valueOf(can[i]);
            }
        }
        
    }//GEN-LAST:event_jButtonComprarActionPerformed

    private void jButtonCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCancelarActionPerformed
        boolean validar = cancelar[0].equals(jLabelCantidad.getText()) && cancelar[1].equals(jLabelCantidad1.getText()) && cancelar[2].equals(jLabelCantidad2.getText()) && cancelar[3].equals(jLabelCantidad3.getText()) && cancelar[4].equals(jLabelCantidad4.getText()) && cancelar[5].equals(jLabelCantidad5.getText()) && cancelar[6].equals(jLabelCantidad6.getText()) && cancelar[7].equals(jLabelCantidad7.getText()) && cancelar[8].equals(jLabelCantidad8.getText()) && cancelar[9].equals(jLabelCantidad9.getText());
        if(!validar){
            int dialog = JOptionPane.YES_NO_OPTION;
            int result = JOptionPane.showConfirmDialog(null, "Quiere cancelar su compra?", "EXIT", dialog);
            if(result == 0){
                jLabelCantidad.setText(cancelar[0]);
                jLabelCantidad1.setText(cancelar[1]);
                jLabelCantidad2.setText(cancelar[2]);
                jLabelCantidad3.setText(cancelar[3]);
                jLabelCantidad4.setText(cancelar[4]);
                jLabelCantidad5.setText(cancelar[5]);
                jLabelCantidad6.setText(cancelar[6]);
                jLabelCantidad7.setText(cancelar[7]);
                jLabelCantidad8.setText(cancelar[8]);
                jLabelCantidad9.setText(cancelar[9]);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Primero debe agregar compras");
        }
    }//GEN-LAST:event_jButtonCancelarActionPerformed

