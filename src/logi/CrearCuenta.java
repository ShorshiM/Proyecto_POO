    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        String contraseña = jTextContraseña.getText();
        String confContraseña = jTextConfirmarContraseña.getText();
        String nombre = jTextNombre.getText();
        String apellido = jTextApellido.getText();
        String email = jTextCorreo.getText();
        String usuario = jTextUsuario.getText();
        if(nombre.isEmpty() && contraseña.isEmpty() && confContraseña.isEmpty() && apellido.isEmpty() && email.isEmpty() && usuario.isEmpty()){
            this.dispose();
            log.setVisible(true);
        }else{
            int dialog = JOptionPane.YES_NO_OPTION;
            int result = JOptionPane.showConfirmDialog(null, "Hay campos llenados Seguro que quieres salir?", "EXIT", dialog);
            if(result == 0){
                this.dispose();
                log.setVisible(true);
            }
        }
        
    }//GEN-LAST:event_jButtonLoginActionPerformed

