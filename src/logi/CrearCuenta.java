    private void jTextUsuarioKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextUsuarioKeyReleased
        if(jTextUsuario.getText().isEmpty()){
            jTextUsuario.setBackground(rojoo);
        }else{
            int result = metodosSql.compararUsuario(jTextUsuario.getText());
            if(result==1){
                jTextUsuario.setBackground(rojis);
            }else{
                jTextUsuario.setBackground(verdesito);
            }
        }  
    }//GEN-LAST:event_jTextUsuarioKeyReleased

