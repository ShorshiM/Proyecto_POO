    private void jButtonCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCrearActionPerformed
        String contraseña = jTextContraseña1.getText();
        String confContraseña = jTextConfirmarContraseña.getText();
        String nombre = jTextNombre1.getText();
        String apellido = jTextApellido.getText();
        String email = jTextCorreo.getText();
        String usuario = jTextUsuario.getText();
        validar();
        if(contraseña.equals("") || confContraseña.equals("") || nombre.equals("") || apellido.equals("") || email.equals("") || usuario.equals("")){
            JOptionPane.showMessageDialog(null,"Los campos resaltados son obligatorios");
        }else{
            if(aceptar){
                JOptionPane.showMessageDialog(null,"Correo no valido");
            }else{
                if(contraseña.equals(confContraseña)){
                    int result = metodoSql.compararUsuario(usuario);
                    if(result==0){
                        int i = metodoSql.guardar(nombre, apellido, email, contraseña, usuario);
                        if(i>0){
                            JOptionPane.showMessageDialog(this,"¡Cuenta creada exitosamente!");
                            animacion.jPanelYArriba(30, -470, 10, 5, jPanelCrear);
                            animacion.jPanelXDerecha(-770, 0, 10, 5, jPanelInterface);
                            animacionInicial().start();
                            limpiar();
                        }else{
                            JOptionPane.showMessageDialog(this,"¡Error en la creacion de cuenta!");
                        }
                    }else{
                        JOptionPane.showMessageDialog(null, "Ese usuario ya existe. Ingrese otro");
                    }
                }else{
                    JOptionPane.showMessageDialog(null,"Las contraseñas no coinciden");
                }
            }
        }
    }//GEN-LAST:event_jButtonCrearActionPerformed

