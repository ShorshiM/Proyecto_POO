    public Timer animacionInicial(){
        Timer time = new Timer(8000, (ActionEvent e) -> {
            AnimationClass animat = new AnimationClass();    
            if(jLabelImagen.getX() == 100){
                //primero
                jLabelImagen.setLocation(-400, 0);
                animat.jLabelXRight(-400, 100, 10, 10, jLabelImagen1);
            }
            if(jLabelImagen1.getX() == 100){
                //segundo
                jLabelImagen1.setLocation(-400, 0);
                animat.jLabelXRight(-400, 100, 10, 10, jLabelImagen2);
            }
            if(jLabelImagen2.getX() == 100){
                //tercero
                jLabelImagen2.setLocation(-400, 10);
                animat.jLabelXRight(-400, 100, 10, 10, jLabelImagen3);
            }
            if(jLabelImagen3.getX() == 100){
                //cuarto
                jLabelImagen3.setLocation(-400, -90);
                animat.jLabelXRight(-400, 100, 10, 10, jLabelImagen);
            }
        });
        return time;
    }
    
