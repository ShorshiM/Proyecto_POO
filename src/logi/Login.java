    private void jButtonIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIngresarActionPerformed
        animacionInicial().stop();
        String nombre = jTextNombre.getText();
        String contraseña = jTextContraseña.getText();
        if(nombre.equals("") || contraseña.equals("")){
            JOptionPane.showMessageDialog(null,"¡Debe llenar las dos casillas!");
        }else{
            if(contador<3){
                if(nombre.equals("Administrador") && contraseña.equals("root")){
                    Form_principal ventana = new Form_principal();
                    this.dispose();
                    ventana.setVisible(true);
                }else{
                    String busca = Metodos_sql.buscarUsuario(nombre, contraseña);
                    if(busca.equals("Bienvenido")){
                        String nom = Metodos_sql.buscarNombre(nombre);
                        JOptionPane.showMessageDialog(null,"Bienvenido "+nom);
                        Cuenta cuentapanel = new Cuenta();
                        cuentapanel.jMenuUsuario.setText("Usuario: "+nom);
                        this.dispose();
                        cuentapanel.setVisible(true);
                        cuentapanel.setjLabelBienvenida(nom);
                        cuentapanel.setExtendedState(cuentapanel.MAXIMIZED_BOTH);
                    }else{
                        contador++;
                        JOptionPane.showMessageDialog(null,"Datos Invalidos intento "+contador);
                    }
                }
            }else{
                JOptionPane.showMessageDialog(null,"Numero de intentos exedidos");
                this.dispose();
            }
        }  
        
    }//GEN-LAST:event_jButtonIngresarActionPerformed

